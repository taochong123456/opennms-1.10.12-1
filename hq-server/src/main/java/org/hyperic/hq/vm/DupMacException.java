package org.hyperic.hq.vm;

public class DupMacException extends Exception {

    public DupMacException() {
        super();
    }

    public DupMacException(String message, Throwable cause) {
        super(message, cause);
    }

    public DupMacException(String message) {
        super(message);
    }

    public DupMacException(Throwable cause) {
        super(cause);
    }

}
